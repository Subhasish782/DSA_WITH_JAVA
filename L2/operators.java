public class operators {
    public static void main(String args[]) {
        //*Binary opearators:-

        int a = 10;
        int b=20;

        System.out.println("a + b = " + (a + b));

        //*Uniary operators:-

        //pre increment:-

        int x=10;
        int y= ++ x;
        System.out.println(x);
        System.out.println(y);

        //post increment:-

        int m=10;
        int n=m ++;

        System.out.println(m);
        System.out.println(n);


    }
}